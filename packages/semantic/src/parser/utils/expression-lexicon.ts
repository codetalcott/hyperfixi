/**
 * Expression-internal vocabulary lexicon (foreign surface ΓåÆ English keyword).
 *
 * LokaScript translates COMMAND and ROLE-MARKER vocabulary via the pattern
 * matchers and `joinTokenText` normalization, but the LEAVES of an expression ΓÇö
 * possessive property names, comparison/existence operators, connectives ΓÇö are
 * captured verbatim from the source language. When the parse is rendered (or
 * executed) as English, those leaves leak the foreign surface: `put my value`
 * authored as es `poner mi valor` rendered `put my valor` (invalid English) and
 * would read a non-existent `.valor` DOM property at runtime.
 *
 * This module is the reverse lexicon the expression layer consults to normalize
 * those leaves to English, so the rendered/executed expression is canonical. It
 * fixes both production paths at once (foreignΓåÆEnglish transpile AND
 * foreignΓåÆASTΓåÆexecute), which is why the normalization happens at PARSE time
 * (see the foreignΓåÆEnglish canonical-validity burndown,
 * docs-internal/EXPRESSION_INTERNAL_TRANSLATION_SCOPE.md).
 *
 * Covers possessive property NAMES, expression CONNECTIVES, and the positional
 * LOCATIVE, plus `joinExpressionTokens` ΓÇö the single seam every raw-expression
 * capture joins through. Surfaces come from the per-language i18n dictionaries
 * (`packages/i18n/src/dictionaries/{code}.ts`) ΓÇö semantic is upstream of i18n in
 * the build order, so the subset is copied here by
 * `packages/i18n/scripts/extract-property-lexicon.ts`. Regenerate with that
 * script if the dictionaries gain translations. Identity surfaces need no entry:
 * an unlisted one passes through unchanged.
 *
 * The invariant to preserve when extending: translation is ANCHORED to a slot,
 * never applied to a free token, unless the lexicon has vouched for the surface
 * as unambiguous. An expression is mostly code ΓÇö user variables, string literals,
 * selectors ΓÇö and a wrong rewrite here is silent.
 */

